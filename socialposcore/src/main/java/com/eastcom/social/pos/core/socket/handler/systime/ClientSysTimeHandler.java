package com.eastcom.social.pos.core.socket.handler.systime;

import io.netty.channel.ChannelHandlerContext;

import com.eastcom.social.pos.core.socket.exception.ChecksumErrorException;
import com.eastcom.social.pos.core.socket.handler.ClientHandlerBase;
import com.eastcom.social.pos.core.socket.listener.systime.SysTimeRespListener;
import com.eastcom.social.pos.core.socket.message.SoCommand;
import com.eastcom.social.pos.core.socket.message.SoMessage;
import com.eastcom.social.pos.core.socket.message.systime.SysTimeRespMessage;

public class ClientSysTimeHandler extends ClientHandlerBase {

	SysTimeRespListener sysTimeRespListener;

	public ClientSysTimeHandler(SysTimeRespListener sysTimeRespListener) {
		this.sysTimeRespListener = sysTimeRespListener;
	}

	@Override
	public void channelActive(ChannelHandlerContext ctx) throws Exception {

	}

	@Override
	public void channelRead(ChannelHandlerContext ctx, Object msg) throws Exception {
		SoMessage message = (SoMessage) msg;

		if (message != null && message.getCommand() == SoCommand.回应获取系统时间) {
			handleSysTimeRespMessage(message, ctx);
		} else {
			ctx.fireChannelRead(msg);
		}
	}

	@Override
	public void exceptionCaught(ChannelHandlerContext ctx, Throwable cause) {
		ctx.fireExceptionCaught(cause);
	}

	protected void handleSysTimeRespMessage(SoMessage msg, ChannelHandlerContext ctx) throws Exception {
		SysTimeRespMessage message = new SysTimeRespMessage(msg);

		System.out.println("Server to SB --> SysTimeRespMessage : ");
		System.out.println(message);
		System.out.println(message.toHexString());
		System.out.println();

		if (message.validateChecksum() == false) {
			throw new ChecksumErrorException("校验码错误");
		}

		if (sysTimeRespListener != null) {
			sysTimeRespListener.handlerRespMessage(message);
		} else if (messageRespListener != null) {
			messageRespListener.handlerRespMessage(msg);
		}

		SendNotice(message);
	}

}